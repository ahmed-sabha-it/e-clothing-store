// SIMPLE FUNCTIONS TO GET PRODUCTS
export const getProductsByCategory = (category) => {
  // Get all products combined
  const allProducts = [...normalProducts, ...salesProducts, ...newArrivalProducts];
  
  if (category === 'all') return allProducts;
  if (category === 'sale') return salesProducts;
  if (category === 'new-arrivals') return newArrivalProducts;
  
  // For specific categories, get normal + sale + new products for that category
  return allProducts.filter(p => p.category === category);
};

export const getProductById = (id) => {
  const allProducts = [...normalProducts, ...salesProducts, ...newArrivalProducts];
  return allProducts.find(p => p.id === id);
};
